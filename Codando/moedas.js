function somarMoedasDe25(quantidadeDeMoedas) {
    var numeroDeMoedas = 0;
    for ( var i = 0; i < quantidadeDeMoedas; i++) {
        numeroDeMoedas = numeroDeMoedas + 0.25
    }

    return numeroDeMoedas
}

